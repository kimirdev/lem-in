#include "libft.h"

int	ft_str_is_printable(char *str)
{
	while (*str == '\0')
		return (1);
	while (*str != '\0')
	{
		if (*str < ' ' || *str > '~')
			return (0);
		str = str + 1;
	}
	return (1);
}
