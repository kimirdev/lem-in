char	*ft_strrev(char *s)
{
	int		i;
	int		l;
	char	c;

	l = 0;
	i = 0;
	while (s[i] != '\0')
		i++;
	i--;
	while (l < i)
	{
		c = s[i];
		s[i] = s[l];
		s[l] = c;
		l++;
		i--;
	}
	return (s);
}
