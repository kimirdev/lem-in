#include "libft.h"

void	ft_putnbr_fd(int nb, int fd)
{
	long int	cp;
	char		s;

	cp = nb;
	if (cp < 0)
	{
		write(fd, "-", 1);
		cp = cp * (-1);
	}
	if ((cp / 10) > 0)
	{
		ft_putnbr_fd(cp / 10, fd);
	}
	s = cp % 10 + '0';
	write(fd, &s, 1);
}
