#include "libft.h"

void	ft_putnbr(int nb)
{
	long int	cp;
	char		c;

	cp = nb;
	if (cp < 0)
	{
		write(1, "-", 1);
		cp = cp * (-1);
	}
	if ((cp / 10) > 0)
	{
		ft_putnbr(cp / 10);
	}
	c = cp % 10 + '0';
	write(1, &c, 1);
}
