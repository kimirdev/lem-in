#include "libft.h"

int	ft_ft_nword(char const *s, char c)
{
	int count;
	int i;

	i = 0;
	count = 0;
	while (s[i] != '\0')
	{
		if (s[i] != c)
		{
			while (s[i] != c && s[i + 1] != '\0')
			{
				i++;
			}
			count++;
		}
		i++;
	}
	return (count);
}
